// Step 1: Use arithmetic
let num1 = 8;
let num2 = 3;

console.log("Addition:", num1 + num2);
console.log("Subtraction:", num1 - num2);
console.log("Multiplication:", num1 * num2);
console.log("Division:", num1 / num2);
console.log("Remainder:", num1 % num2);

// Step 2: Use string concatenation
let firstName = "Adrian";
let hobby = "Gaming";

console.log("Hi, I'm " + firstName + " and I like " + hobby + ".");

// Step 3: Use const
const country = "USA";
console.log("I live in the " + country);

// Step 4: Use typeof
console.log("Type of firstName:", typeof firstName);
console.log("Type of num1:", typeof num1);
console.log("Type of country:", typeof country);